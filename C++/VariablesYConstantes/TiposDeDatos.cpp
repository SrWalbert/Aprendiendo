#include <iostream>

using namespace std;
int main(int argc, char const *argv[])
{
  //variable tipo entero
  int entero = 44;
  cout << entero;
  cout << "\n";
  //variable flotantes
  float flotante = 4.3332;
  cout << flotante;
  cout << "\n";
  //tipo doble
  double doble = 3.1415;
  cout << doble;
  cout << "\n";
  // caracter
  char letra = 'A';
  cout << letra;
  cout << "\n";
  //booleano
  bool boleano = true;
  cout << boleano;
  cout << "\n";

  return 0;
}
